'''
У цьому прикладі name — це позиційний параметр, 
а message — ключовий параметр зі значенням 
за замовчуванням "Привіт". При виклику функції 
можна не вказувати message, і тоді буде 
використано значення за замовчуванням.
'''
def greet(name, message="Hello"):
    print(f"{message}, {name}!")

# використовує значення за замовчуванням для message
greet("Serhii")  

# передача власного значення для message
greet("Nastia", message="God morning")  

