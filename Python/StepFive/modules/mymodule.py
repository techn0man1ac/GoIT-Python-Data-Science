def say_hello(name):
    print(f'Hello {name}')

def main():
    print("You imported hello.py")
    say_hello('user')

if __name__ == '__main__':
    main()

'''
Так заведено і функцію main ще називають "точкою входу", 
оскільки робота застосунку починається з виклику цієї функції. 
Ви, звичайно, можете назвати цю функцію як завгодно, але 
називати її саме main вважається хорошим тоном.
'''